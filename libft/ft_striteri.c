#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	if (!s || !f)
		return ;
	i = 0;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}

/* Esta función aplica la función f a cada caracter de s, dando como parámetros 
 * el indice de cada caracter y su dirección, que podrá modificarse*/